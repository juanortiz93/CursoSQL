CREATE DEFINER=`root`@`localhost` FUNCTION `verificar_existencia_cupon`(p_codigoCupon VARCHAR(45)) RETURNS tinyint(1)
    DETERMINISTIC
BEGIN
	DECLARE existe BOOLEAN;
    
    SELECT EXISTS (
        SELECT 1
        FROM cupon
        WHERE codigoCupon = p_codigoCupon AND estadoCupon = 'Activo'
    )
    INTO existe;
    
    RETURN existe;
